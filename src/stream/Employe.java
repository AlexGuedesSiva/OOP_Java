package stream;

public class Employe {

    final String nome;
    final double salario;

    final boolean absenteismoNull;

    public Employe(String nome, double salario, boolean absenteismoNull) {
        this.nome = nome;
        this.salario = salario;
        this.absenteismoNull = absenteismoNull;
    }
}
